package org.jclouds.dimensiondata.cloudcontroller.domain;

import org.jclouds.javax.annotation.Nullable;
import org.jclouds.json.SerializedNames;

import com.google.auto.value.AutoValue;

@AutoValue
public abstract class IpRange {

    IpRange() {} // For AutoValue only!

    public abstract String address();
    @Nullable public abstract Integer prefixSize();

    @SerializedNames({ "address", "prefixSize" })
    public static IpRange create(String address, Integer prefixSize) {
        return new AutoValue_IpRange(address, prefixSize);
    }
}